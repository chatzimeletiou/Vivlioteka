    private void rbSelectUser(){
        
        int row = rbuTable.getSelectedRow();
        rbuAM.setBackground(Color.white);
        rbbCompleteButton.setEnabled(false);
        
        if(row==-1){
            emptyRBULabels();
            rbbTable.setEnabled(false);
            rbbSelectButton.setEnabled(false);
            rbbCompleteButton.setEnabled(false);
            return;
        }
        
        int id = Integer.parseInt(rbuTable.getValueAt(row, 0).toString());
        
        String rqst = "select customerid, cname, surname, telephone, studentid,idcardnumber,birthdate from customer where customerid="+id;
        int am=0;
        try {
            rs1 = statement.executeQuery(rqst);
            rs1.next();
            rbuID.setText(rs1.getInt(1)+"");
            rbuName.setText(rs1.getString(2)+" "+rs1.getString(3));
            rbuTelephone.setText(rs1.getLong(4)+"");
            am=rs1.getInt(5);
            if(am!=0)
                rbuAM.setBackground(Color.green);    
            rbuAM.setText(am+"");
            rbuIdCardNum.setText(rs1.getString(6));
            rbuDate.setText(trimDate(rs1.getString(7),10));
        } catch (SQLException ex) {
            Logger.getLogger(Log_In.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String countBooks = "select count(*) from rental where customerid="+id+" and status='p'";
        
        try {
            rs1=statement.executeQuery(countBooks);
            rs1.next();
            rbuBooks.setText(rs1.getInt(1)+"");
        } catch (SQLException ex) {
            Logger.getLogger(Log_In.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        rbbSelectButton.setEnabled(true);
        rbbTable.setEnabled(true);
        fillRBBTable();
    }
    
