    private void fillRBBLabels(){
        int row = rbbTable.getSelectedRow();
        
        if(row==-1){
            rbbCompleteButton.setEnabled(false);
            emptyRBBLabels();
            return;
        }
        
        int id = Integer.parseInt(rbbTable.getValueAt(row, 0).toString());
        String date = rbbTable.getValueAt(row,2).toString();
        int cid = Integer.parseInt(rbuID.getText());
        
        String rqst = "select r.bookid, b.name, r.rentdate from book b join rental r on (b.bookid=r.bookid) where b.bookid="+id+" and r.rentdate=TO_DATE('"+date+"','YYYY-MM-DD')";
        try {
            rs1 = statement.executeQuery(rqst);
            rs1.next();
            rbbID.setText(rs1.getInt(1)+"");
            rbbName.setText(rs1.getString(2));
            rbbDate.setText(date);
        } catch (SQLException ex) {
            Logger.getLogger(Log_In.class.getName()).log(Level.SEVERE, null, ex);
        }

        String getDays = "select trunc(current_date-rentdate),b.rentalpriceperday as days from dual,rental r join book b on(r.bookid=b.bookid) join customer c on (r.customerid=c.customerid) where r.bookid="+id+" and r.rentdate=To_date('"+date+"','YYYY-MM-DD') and r.customerid="+cid+" and r.status='p'";
        
        int days = 0;
        double price = 0;
        
        try {
            rs1 = statement.executeQuery(getDays);
            rs1.next();
            days=rs1.getInt(1);
            price=rs1.getDouble(2);
            rs1=null;
        } catch (SQLException ex) {
            Logger.getLogger(Log_In.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(!rbuAM.getText().equals("0"))
            rbbPrice.setText(days*price*0.80+"€");
        else
            rbbPrice.setText(days*price+"€");
        rbbCompleteButton.setEnabled(true);
    }
    
